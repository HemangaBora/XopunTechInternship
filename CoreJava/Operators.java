package CoreJava;

//Arithmetic Operators

class Main {
    public static void main(String[] args) {
      
      // declare variables
     int a = 12, b = 5;
  
      // addition operator
      System.out.println("a + b = " + (a + b));
    }
}

//Assignment Operators

class Main {
    public static void main(String[] args) {
      
      // create variables
      int a = 4;
      int var;
  
      // assign value using =
      var = a;
      System.out.println("var using =: " + var);
    }
}



